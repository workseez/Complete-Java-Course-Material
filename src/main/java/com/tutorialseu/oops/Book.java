package com.tutorialseu.oops;

public class Book {
    // Member variables
    String title;
    String author;
    int numPages;

    // Method to print book details
    public void printBookDetails() {
        System.out.println("Title: " + title);
        System.out.println("Author: " + author);
        System.out.println("Number of Pages: " + numPages);
    }
}
